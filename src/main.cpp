#include <Adafruit_MPU6050.h>
#include <Adafruit_Sensor.h>
#include <Wire.h>

// Can pick any GPIO for I2C on ESP32
// Default ones on ESP32-CAM are used by camera and aren't exposed
#define I2C_DATA 12
#define I2C_CLK  14

Adafruit_MPU6050 mpu;

